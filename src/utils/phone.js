/** Нормальное отображение телефона горячей линии */
export const HOTLINE_LOCAL = '0 228 00-77-00'
export const HOTLINE_INTERNATIONAL = '+996 (228) 00-77-00'

export function formatPhoneLocal() {
  return HOTLINE_LOCAL
}

export function formatPhoneInternational() {
  return HOTLINE_INTERNATIONAL
}
